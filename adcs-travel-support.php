	  <h1>Travel support -Travel Awards for Students Presenting at the Seventeenth Australasian Document 
Computing Symposium (ADCS2012)</h1>
<p>The Conference is offering travel support to students coming to ADCS2012 in 
Dunedin. These awards can be put toward travel and accommodation expenses. 
The organising committee reserves the right to make awards as they see 
fit, or to alter the amount of any awards.</p>
<H3>Students: How to apply</H3>
<p>Send an email to Andrew Turpin, <a 
href="mailto:aturpin@unimelb.edu.au">aturpin@unimelb.edu.au</a>) indicating that you 
would like to apply for the award, and the amount for which you are applying. 
The email <em>must</em> be cc'ed to your supervisor. In the email, you should 
briefly outline why you should get the award. Bullet points addressing the 
selection criteria below would be excellent. Be sure to include your name, your 
supervisor's name, and where you will be travelling from.</p>
<P>You must also arrange for your supervisor to send a separate email 
acknowledging that you are indeed enrolled as a student, and that the statements 
you made in the application are correct (to the best of their knowledge).</P>
<P><EM>Note that the award will be in the form of a reimbursement of expenses, 
not a cash award</EM>. The awardees will only receive money after producing 
receipts for appropriate expenses, and completing any supplied forms. </P>
<H3>Selection Criteria</H3>
<P>Applications will be selected according to the following criteria.</P>
<P>Essential:</P>
<OL>
  <LI>The student is presenting a paper at ADCS2012. (You can apply for an award 
  before paper decisions are announced, as long as you have submitted a paper; 
  and as long as you will be presenting it, if the paper is accepted.) 
  <LI>The student's host university and/or supervisor is unable to fund (or 
  fully fund) the travel. 
  <LI>The student will stay in cheap accommodation during the conference. 
</LI></OL>
<p>Desirable:</p>
<ol>
  <li>The student has not attended ADCS in previous years. 
  <li>The student has not attended any conferences during their candidature and 
  is nearing the end of their candidature. </li></ol>
<h3>Closing Date</h3>
<p>Award applications must be received no later than 11 November 2012.</p>
	 
