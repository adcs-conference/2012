	  <h1>ADCS2012 - Paper Guidelines</h1>
	  
 
	  
<p>Submitted papers must be in English, should be typed in two-column,
single-spaced format, and must adhere to the word limits in the Call
for Papers.  They must include the author's name, affiliation and
e-mail address, a 200 word abstract, and keywords identifying the
paper's area. The keywords listed in the Call for Papers are
preferred:</p>

<ul>
  <li>Cognitive Aspects of Documents</li>
  <li>Digital Libraries</li> 
  <li>Document Databases</li> 
  <li>Document Management</li> 
  <li>Document Standards (XML, SGML etc)</li> 
  <li>Document Workflow</li> 
  <li>Information Retrieval</li> 
  <li>Multimedia Document Management</li> 
  <li>Multimedia Resource Discovery</li> 
  <li>Natural Language Techniques and Documents</li> 
  <li>Personalised Documents</li> 
  <li>User Studies Involving Documents</li>
  <li>Web Documents </li>
</ul>

<br/>
<h2>Paper Templates</h2>
<p><img src=ACM_ICPS_sm.jpg border="0" alt="ACM - ICPS"> </p>  


<p>Please use the Word or LaTeX template files which are available 
on the <a href="http://www.acm.org/sigs/publications/proceedings-templates">ACM templates page</a>.</p>

<p>Submissions of full-length papers should be a maximum of eight pages in 
the required style (approximately 3000 words).  </p>
<p>
Submissions for Posters,
Short papers, Industry Status Reports, and Demonstrations (if any of
the above) should be a maximum of four pages in
the required style (appoximately 1500 words).</p>

<p>All submissions must be original work, not previously published 
elsewhere, and not currently submitted to any other conference or
journal.</p>

<p>Submissions that differ markedly in length or style from these
guidelines risk being rejected without further consideration.</p>

<p>
Paper submission will be through <a href="https://www.easychair.org/conferences/?conf=adcs20121">EasyChair</a>.

