	  <H1>Dunedin, New Zealand</H1>
<div>
<p>Dunedin (Dun Edin) is the ancient Celtic name for Edinburgh. Situated on the south east coast of the South Island, 
the port city stands at the head of Otago Harbour beneath a rugged landscape of steep hills. 
Dunedin was founded in 1848 as a Free Church of Scotland settlement and was chosen because of 
its timber resources and farming potential. The discovery of gold in 1861 in central 
Otago district brought great wealth to the town and during the second half of the 19th 
century it was the largest town in New Zealand. Today, it is the fifth largest town in 
New Zealand with a population of 120,000.
</p>
<p><img SRC="images/dunedin1.jpg" ALT="Dunedin at night"></p>
<p>Dunedin is New Zealand's most popular university city. The <a HREF="http://www.otago.ac.nz/">University of Otago</a>, 
which currently has over 20,000 students enrolled, includes one of New Zealand's two medical schools and the only dental school.  
Founded in 1869, it is the oldest university in New Zealand. The <a HREF="http://www.tekotago.ac.nz/">Otago Polytechnic 
Institute</a> is also located in Dunedin. The presence of so many students has encouraged a lively cafe and pub 
culture and propelled a strong <a HREF="http://www.id-dunedinfashion.com/">fashion design industry</a>.
</p>
<p>Businesses in Dunedin capitalise on a skilled work force and tertiary research strengths. Information 
technology developed here by <a HREF="http://www.arl.co.nz/">Animation Research Ltd</a>, produced the ground-breaking 
America's Cup real-time computer graphics. Precision engineering enterprises include the cutting edge robotics 
of <a HREF="http://www.scott.co.nz/">Scott Technology</a>. <a HREF="http://www.nhnz.tv/home">Natural 
History New Zealand</a> has teams all over the planet at any time, producing some of the world's finest natural 
history and wildlife films and television programmes. Biotechnology and associated agricultural research is 
carried out at the Invermay Research Centre near Mosgiel.
						</p>
						<p>The city has a beautiful green Town Belt, which was planned by the city founders to surround the inner city with forest. Dunedin's <a HREF="http://www.visit-dunedin.co.nz/historic_buildings.html">architecture</a> is a combination of Victorian and modern buildings. Dunedin has a botanical garden close to the university campus, the <a HREF="http://dunedin.art.museum/">Dunedin Public Art Gallery</a> in the centre of town, and the <a HREF="http://www.otagomuseum.govt.nz/">Otago Museum</a>.
						</p>
						<p><img SRC="images/stclair.jpg" ALT="St Clair Beach"></p>
						<p>A popular attraction on the Otago Peninsula are the colonies of many rare wildlife species, including the <a HREF="http://www.albatrosses.co.nz/">Royal Albatross</a>, Southern Fur Seals, cormorants and <a HREF="http://www.penguin-place.co.nz/">Yellow-Eyed penguins</a>. Nature and wildlife tour companies run daily excursions to view these animals up close in their natural habitat. There are plenty of walking tracks around the hills providing access to special areas of coastline, historic sites, and spectacular views of the peninsula and harbour.
						</p>
						<p>For those who enjoy water sports, Otago harbour is perfect for wind surfing, yachting, kayaking, canoeing, rowing and fishing. Surfing is becoming an increasingly popular sport and St Clair beach is considered one of New Zealand's best for surfing.
						</p>
