<h3>Invited Talk 1</h3>
<p>Charles L. A. Clarke of the University of Waterloo is to give a keynote address entitled 'Time-Biased Gain'.</p>

<p>
Time-biased gain provides a unifying framework for information retrieval evaluation, generalizing many traditional 
effectiveness measures while accommodating aspects of user behavior not captured by these measures. 
By using time as a basis for calibration against actual user data, time-biased gain can reflect aspects 
of the search process that directly impact user experience, including document length, near-duplicate 
documents, and summaries. Unlike traditional measures, which must be arbitrarily normalized for averaging 
purposes, time-biased gain is reported in meaningful units, such as the total number of relevant documents 
seen by the user. In work reported at SIGIR 2012, we proposed and validated a closed-form equation for 
estimating time-biased gain, explored its properties, and compared it to standard approaches. 
In work reported at CIKM 2012, we used stochastic simulation to numerically approximate 
time-biased gain, an approach that provides greater flexibility, allowing us to accommodate 
different types of user behavior and increases the realism of the effectiveness measure.  
In work reported at HCIR 2012, we extended our stochastic simulation to model the 
variation between users.  In this talk, I will provide an overview of time-biased gain, 
and outline our ongoing and future work, including extensions to evaluate query suggestion, 
diversity, and whole-page relevance.  This is joint work with Mark Smucker.</p>

<p><i>
<b>Charles Clarke</b> is a Professor in the School of Computer Science at the 
University of Waterloo, Canada.  His research interests include information 
retrieval, web search, and text data mining.  He has published on a wide 
range of topics, including papers related to question answering, XML, 
filesystem search, user interfaces, statistical natural language 
processing, and the evaluation of information retrieval systems.  
He was a Program Co-Chair of the 2007 ACM SIGIR Conference on Research and Development 
in Information Retrieval; he was the General Co-Chair of SIGIR in 2003.  
He received his Ph.D. from Waterloo in 1996.  From 1996 to 1999 he was an 
Assistant Professor in the Department of Electrical and Computer Engineering 
at the University of Toronto.  He has previously held software development 
positions at a number of computer consulting and engineering firms.  
In 2006 he spent a sabbatical at Microsoft, where he was involved in the 
development of what is now the Bing search engine. He is a co-author of the 
book Information Retrieval: Implementing and Evaluating Search Engines, MIT Press, 2010.
</i></p>

<h3>Invited Talk 2</h3>

<p>Dr. Nigel Stanger, Department of Information Science, University of Otago, will give a talk entitled 'The Challenges of Building Online Community Museums'.</p>

<p>Over the past few years I have been involved in the development of online
museums for two different communities in Central Otago, New Zealand.
Developing digital archives for the general public raises some interesting
issues, including usability, copyright, curation of items, and potentially
dealing with unusual document types. In this talk I will discuss these and
other issues, and chronicle the development of both museums.</p>

<p><i>
Dr. Nigel Stanger is a lecturer in the Department of Information Science at
the University of Otago School of Business, where he has taught database
systems since 1989. He has research interests in digital preservation,
digital repositories, relational theory, XML technologies, physical database
design and database performance.
</i></p>


